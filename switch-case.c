#include <stdio.h>

int main() {
    int n = 5;
    switch(n) {
    case 1 :
        printf("ONE");
        break;
    case 2 :
        printf("TWO");
        break;
    case 3 :
        printf("THREE");
        break;
    case 4 :
        printf("FOUR");
        break;
    default :
        printf("FIVE");
    }
    return 0;
}

